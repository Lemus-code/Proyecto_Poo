    private void desactivarCeldas(){
        for (int i = 0; i < modelo.getColumnCount(); i++) {
            Class<?> colClass = modelo.getColumnClass(i); //Toma el tipo de dato que tiene una columna, por eso el genérico
            this.jTablaAddons.setDefaultEditor(colClass, null); // Esto deshabilita la edición
        }
        jTablaAddons.getTableHeader().setReorderingAllowed(false); // Desactivar la reordenación de columnas
        jTablaAddons.getTableHeader().setResizingAllowed(false);
    };
     
    private void mostrarModificaciones(){   
        modelo.setRowCount(0);
        for(int i = 0; i < shareData.getCasas().size(); i++){
            if(shareData.getCasas().get(i).getNombreCliente().equals(cliente)){
                for(int k = 0; k < shareData.getCasas().get(i).getAddons().size(); k++){

                    String [] row = new String[6];
                    row[0] = shareData.getCasas().get(i).getAddons().get(k).getNombreModi();
                    row[1] = "<html><body style='width: 300px'>" + shareData.getCasas().get(i).getAddons().get(k).getDescrip();
                    row[2] = shareData.getCasas().get(i).getAddons().get(k).getEstado();
                    row[3] = String.valueOf(shareData.getCasas().get(i).getAddons().get(k).getFechaInicio());
                    row[4] = String.valueOf(shareData.getCasas().get(i).getAddons().get(k).getFechaFin());
                    row[5] = String.valueOf(shareData.getCasas().get(i).getAddons().get(k).getCostoAdicional());
                    modelo.addRow(row);
                    
                }
            }
        }
        ajustarTamanoColumnaDescripcion();
    } 
    private void ajustarTamanoColumnaDescripcion() {
        TableColumn columnaDescripcion = jTablaAddons.getColumnModel().getColumn(getColumnIndex("Descripción"));
        int ancho = 0;
        int alto = 1; // Altura mínima
        TableCellRenderer renderer = columnaDescripcion.getHeaderRenderer();

        for (int row = 0; row < jTablaAddons.getRowCount(); row++) {
            renderer = jTablaAddons.getCellRenderer(row, columnaDescripcion.getModelIndex());
            Component comp = renderer.getTableCellRendererComponent(jTablaAddons, jTablaAddons.getValueAt(row, columnaDescripcion.getModelIndex()), false, false, row, columnaDescripcion.getModelIndex());
            ancho = Math.max(comp.getPreferredSize().width, ancho);

            // Calcula la altura requerida para el contenido de la celda
            int rowHeight = comp.getPreferredSize().height;
            alto = Math.max(rowHeight, alto);
         }

        columnaDescripcion.setPreferredWidth(ancho + 10); // Agrega un pequeño espacio adicional.
        jTablaAddons.setRowHeight(alto);
    }
    
     private void calcularCostoModificaciones(){
        double costoAddons = 0;
        for(int i = 0; i < shareData.getCasas().size(); i++){
            if(shareData.getCasas().get(i).getNombreCliente().equals(cliente)){
                for(int k = 0; k < shareData.getCasas().get(i).getAddons().size(); k++){
                      costoAddons = costoAddons + shareData.getCasas().get(i).getAddons().get(k).getCostoAdicional();
                }
                shareData.getCasas().get(i).setCostoAddons(costoAddons);
            }
        }
       
    }
